package prakt6;

public class Point{
    public int current_x;
    public int current_y;
    public int last_x;
    public int last_y;

    public Point(int _current_x,
            int _current_y,
            int _last_x,
            int _last_y){
        current_x = _current_x;
        current_y = _current_y;
        last_x = _last_x;
        last_y = _last_y;
    }
}
